#Operadores
print(4 + 3)
print(4 - 3)
print(4 * 3)
print(4 / 3)

print(8 % 3)   #Te da el resto de la división
print(10 // 3) #Hace la división y el valor que devuelve es solo el número entero
print(3**2)    #Hace un 3 elevado a 2

print("Hola " + "Python") #Concatenar textos 

#Operadores Comparativos
print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)

#Operadores lógicos (and, or, not)
print(3 < 4 and 2 < 4)
print(3 < 4 or 6 < 4)
print(not(3 < 4)) #not es para negar la condición